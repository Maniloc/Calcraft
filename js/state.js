// ─────────────────────────────────────────────────────────────
// Вычисляет автоматический перенос праздника с выходного на
// ближайший следующий рабочий день (ТК РФ ст. 112 ч. 2).
//
// Исключение: новогодние каникулы (1-8 янв) и Рождество —
//   их переносит Правительство вручную, не автоматически.
// ─────────────────────────────────────────────────────────────
function computeTransfer(baseDate, allHolidayDates, weekends) {
  const d    = new Date(baseDate + 'T00:00:00');
  const dow  = d.getDay();
  const isWE = weekends.has(dow);
  if (!isWE) return null; // праздник в рабочий день — перенос не нужен

  // Ищем следующий рабочий день, не занятый другим праздником
  const candidate = new Date(d);
  for (let i = 0; i < 14; i++) {
    candidate.setDate(candidate.getDate() + 1);
    const cdow = candidate.getDay();
    const cKey = toDateKey(candidate);
    if (!weekends.has(cdow) && !allHolidayDates.has(cKey)) {
      return cKey;
    }
  }
  return null;
}

function toDateKey(d) {
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  return `${y}-${m}-${day}`;
}

// Праздники которые НЕ переносятся автоматически (Правительство решает вручную)
const NO_AUTO_TRANSFER = new Set([
  '01-01','01-02','01-03','01-04','01-05','01-06','01-07','01-08',
]);

// Строит массив событий из RF_CALENDAR для данного года с автопереносами
export function buildHolidaysForYear(year) {
  const cal = RF_CALENDAR[year];
  if (!cal) return [];

  const weekends = new Set([0, 6]); // Сб/Вс — стандартные выходные для расчёта переносов
  const events   = [];
  let idx        = 0;

  // 1. Собираем все даты (base + govt) для проверки занятости при переносе
  const allDates = new Set([
    ...(cal.base  || []).map(h => h.date),
    ...(cal.govt  || []).map(h => h.date),
    ...(cal.short || []).map(h => h.date),
  ]);

  // 2. Базовые праздники + автоперенос
  for (const h of (cal.base || [])) {
    // Добавляем сам праздник
    events.push({
      id: -(year * 1000 + idx++), name: h.name, date: h.date,
      color: '#C0392B', repeat: false, type: 'holiday', system: true,
    });

    // Проверяем нужен ли автоперенос
    const mmdd = h.date.slice(5); // MM-DD
    if (NO_AUTO_TRANSFER.has(mmdd)) continue; // новогодние — не трогаем

    const transfer = computeTransfer(h.date, allDates, weekends);
    if (transfer) {
      allDates.add(transfer);
      events.push({
        id: -(year * 1000 + idx++),
        name: `Выходной (перенос с ${h.date.slice(8)}.${h.date.slice(5,7)})`,
        date: transfer,
        color: '#C0392B', repeat: false, type: 'holiday', system: true,
      });
    }
  }

  // 3. Правительственные переносы (добавляем как есть)
  for (const h of (cal.govt || [])) {
    events.push({
      id: -(year * 1000 + idx++), name: h.name, date: h.date,
      color: '#C0392B', repeat: false, type: 'holiday', system: true,
    });
  }

  // 4. Предпраздничные дни
  for (const h of (cal.short || [])) {
    events.push({
      id: -(year * 1000 + ID_RF_SHORT + idx++), name: h.name, date: h.date,
      color: '#E67E22', repeat: false, type: 'short', system: true,
    });
  }

  return events;
}


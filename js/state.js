// Строит массив событий из RF_CALENDAR для данного года
export function buildHolidaysForYear(year) {
  const cal = RF_CALENDAR[year];
  if (!cal) return [];

  const events = [];
  let idx = 0;

  (cal.holidays || []).forEach(h => {
    events.push({
      id:     -(year * 1000 + idx++),
      name:   h.name,
      date:   h.date,
      color:  '#C0392B',
      repeat: false,
      type:   'holiday',
      system: true,
    });
  });

  (cal.short || []).forEach(h => {
    events.push({
      id:     -(year * 1000 + 500 + idx++),
      name:   h.name,
      date:   h.date,
      color:  '#E67E22',
      repeat: false,
      type:   'short',
      system: true,
    });
  });

  return events;
}


// Строит список событий для конкретного года
export function buildHolidaysForYear(year) {
  const RED   = '#C0392B';
  const SHORT = '#E67E22';
  const events = [];

  const cal = RU_CALENDAR[year];
  if (cal) {
    // Точный производственный календарь
    cal.holidays.forEach((h, i) => {
      events.push({
        id: -(year * 1000 + i + 1),
        name: h.name,
        date: h.d,
        color: RED,
        repeat: false,
        type: 'holiday',
        system: true,
      });
    });
    cal.short.forEach((h, i) => {
      events.push({
        id: -(year * 1000 + 500 + i),
        name: h.name + ' *',
        date: h.d,
        color: SHORT,
        repeat: false,
        type: 'short',   // предпраздничный, не вычитается из рабочих дней
        system: true,
      });
    });
  } else {
    // Базовые праздники без переносов
    BASE_RU_HOLIDAYS.forEach((h, i) => {
      events.push({
        id: -(year * 1000 + i + 1),
        name: h.name,
        date: `${year}-${h.m}-${h.d}`,
        color: RED,
        repeat: false,
        type: 'holiday',
        system: true,
      });
    });
  }
  return events;
}

